    private void bttnIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnIniciarSesionActionPerformed
        String user = txtUsuario.getText();
        String pass = txtContrasena.getText();
        if (!user.isEmpty() && !pass.isEmpty()) {
            CLSProfesor pro = new CLSProfesor();

            var variablecontenedoradeconsultabd = pro.LoginProfesor(user, pass);

            if (variablecontenedoradeconsultabd == true) {
                JOptionPane.showMessageDialog(null, "Welcome");
            } else {
                JOptionPane.showMessageDialog(null, "Stop");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Llenar los campos");
        }
    }//GEN-LAST:event_bttnIniciarSesionActionPerformed


